def b2f(b):
    return Image.open(io.BytesIO(base64.b64decode(b))).convert("RGB")


def resize(f):
    w, h = f.size
    if w > h:
        p = (w - h) // 2
        f = f.crop([p, 0, p + h, h])
    elif h > w:
        p = (h - w) // 2
        f = f.crop([0, p, w, p + w])
    f = f.resize([512, 512])
    return f


def img2npy(f):
    return (2.0 * np.array(f) / 255.0 - 1.0).transpose((2, 0, 1)).astype(np.float32)


